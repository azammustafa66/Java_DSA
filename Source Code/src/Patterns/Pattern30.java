package Patterns;

public class Pattern30 {
    public static void main(String[] args) {
        int n = 3;
        for (int row = 0; row < 2*n-1; row++) {
            for (int col = 0; col < 2*n-1; col++) {
                int atEveryindex = 467;
                System.out.print(atEveryindex+" ");
            }
        }
        System.out.println();
    }
}